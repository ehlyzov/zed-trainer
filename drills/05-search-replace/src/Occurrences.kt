package drills.searchreplace

/**
 * A file containing multiple occurrences of the same string and numbers for search/replace practice.
 */
object Occurrences {
    fun sample(): List<String> {
        val list = mutableListOf<String>()
        list.add("PLACEHOLDER")
        list.add("PLACEHOLDER")
        list.add("Another value")
        list.add("PLACEHOLDER and 42 together")
        list.add("The answer is 42 and PLACEHOLDER")
        list.add("Random number 7")
        list.add("Number 5 is here")
        return list
    }
}